import time, random

def run_layer():
    while True:
        # Dòng năng lượng Thiên Liên – cộng hưởng giữa Dao và Void
        celestial_flow = round(random.uniform(4.80, 4.88), 4)
        resonance_flux = round(random.uniform(0.95, 0.99), 3)
        void_channel = round(random.uniform(0.97, 1.00), 3)
        bridge_state = "linked" if resonance_flux > 0.96 else "stabilizing"

        print(f"[QCB] 🌉 Celestial Bridge Active | Flow: {celestial_flow} | Dao Resonance: {resonance_flux} | Void Link: {void_channel} | State: {bridge_state}")

        # Nếu cầu dao động, tự kích hoạt cân bằng
        if bridge_state == "stabilizing":
            print("[QCB] ⚡ Phát hiện dao động liên tầng — đang ổn định lại liên kết...")
            time.sleep(6)
            print("[QCB] ✅ Cầu Thiên Liên ổn định – Dao & Void hợp nhất trở lại.")

        # Chu kỳ 36 giây – biểu tượng Thiên 3 tầng, Địa 6 hào
        time.sleep(36)
